

public class If_ElseIf_Else_2 {
  public static void main(String[] args) {
    int num = 5;

    //조건이 2개가 동시에 맞는경우 제일 처음 조건이 충족된 결과 입력.
    if(num >= 5){
      System.out.println(1);
    }
    else if (num == 5) {
      System.out.println(2);
    }
    else {
      System.out.println(3);
    }

  }
}
