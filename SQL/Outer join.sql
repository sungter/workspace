# BOOK_CATEGORY 테이블에 존재하는 모든 카테고리에 대한
# 카테고리 번호, 카테고리이름, 책 이름, 책 가격을 조회
SELECT C.CATE_CODE, CATE_NAME, BOOK_NAME, BOOK_PRICE
FROM book_category C
INNER JOIN book B
ON C.CATE_CODE = B.CATE_CODE
ORDER BY BOOK_PRICE;


SELECT C.CATE_CODE, CATE_NAME, BOOK_NAME, BOOK_PRICE
FROM book_category C
LEFT OUTER JOIN book B
ON C.CATE_CODE = B.CATE_CODE;
