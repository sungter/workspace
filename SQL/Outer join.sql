# 주문자 ID, 주문상품명, 주문수량, 주문자명을 INNER JOIN으로 조회
#INNER JOIN
SELECT O.CUSTOMER_ID, PRODUCT_NAME, ORDER_CNT, NAME
FROM ORDERS O
INNER JOIN CUSTOMER C
ON C.CUSTOMER_ID = O.CUSTOMER_ID;

#서브쿼리
SELECT CUSTOMER_ID
	, PRODUCT_NAME
	, ORDER_CNT
	, (SELECT NAME 
		FROM customer 
		WHERE CUSTOMER_ID = orders.CUSTOMER_ID) AS NAME
FROM orders;

# OUTER JOIN
SELECT C.CUSTOMER_ID, 
	NAME, 
	PRODUCT_NAME, 
	ORDER_CNT
FROM orders O
RIGHT OUTER JOIN customer C
ON C.CUSTOMER_ID = O.CUSTOMER_ID;




